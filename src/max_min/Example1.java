package max_min;
                     /*usage of min and max*/
//class
public class Example1 {
	//main method
	public static void main(String[] args) {
		//creating variables
		double x = 49.5;
		double y = -40;

		//usage of min and max
		double a = Math.min(x, y); // for min 
		double b = Math.max(x, y);  // for Max
		
		//displaying the output
		System.out.println(a);
		System.out.println(b);
	}

}
