package for_loop;
/*++++++++++++++ IN THIS CODE I USED FOR STATEMENT TO COUNT FROM 0 TO 10 +++++++++++++++*/

public class CountingTo10 {

	public static void main(String[] args) {

		// here is my for statement which will execute 11 times from 0 to 10
		for (int i = 0; i <= 10; i++) {

			// in each time the code executed will display the current sequence
			System.out.println(i);
		}

	}

}
